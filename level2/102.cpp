const int N=2000050,M=1000000007;
const ll INF=1e18+7;

bool ok[N];
vector<int>gay;

void solve(){
    int n;
    cin>>n;
    for(int i=3;i<=n;i+=2) ok[i]=true;
    ok[2]=true;
    for(ll i=3;i<=n;i+=2){
        if(!ok[i]) continue;
        for(ll j=i*i;j<=n;j+=2*i) ok[j]=false;
        gay.pb(i);
    }
    for(int x:gay) cout<<x<<" ";
    return;
}

